let grid = [];
const SIZE = 1000;
const mid = SIZE / 2;
const puzzleSize = input[0].length;
const s = mid - Math.floor(puzzleSize / 2);

for (let y = 0; y < SIZE; y++) {
  grid[y] = [];
  for (let x = 0; x < SIZE; x++) {
    grid[y][x] = '.';
    if (x >= s && x < s + puzzleSize) {
      if (y >= s && y < s + puzzleSize) {
        grid[y][x] = input[y - s][x - s];
      }
    }
  }
}
const burst = (x,y, dir, grid, infected) =>{
	if(grid[y][x] === '#'){
  	grid[y][x] = '.';
    if(dir === 'U'){
    	x++;
      dir = 'R';
    }
    else if(dir === 'R'){
    	y++;
      dir = 'D';
    }
    else if(dir === 'D'){
    	x--;
      dir = 'L';
    }
    else if(dir === 'L'){
    	y--;
      dir = 'U';
    }
  }
  else{
  	infected++;
    grid[y][x] = '#';
    if(dir === 'U'){
    	x--;
      dir = 'L';
    }
    else if(dir === 'L'){
    	y++;
      dir = 'D';
    }
    else if(dir === 'D'){
    	x++;
      dir = 'R';
    }
    else if(dir === 'R'){
    	y--;
      dir = 'U';
    }
  }
  return {x,y,dir, infected};
}

